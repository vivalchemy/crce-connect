<h4><strong>|ABOUT OUR JOURNEY IN TIME</strong></h4>

<p>
  Located in a picturesque environment in the heart of the Mumbai city,
  Fr.Conceicao Rodrigues College of Engineering (Fr.CRCE) is one of the
  renowned, premier and sought-after private Engineering colleges. The Institute
  is dedicated to making a difference in Engineering education, with its
  exclusive approach towards the ongoing momentum of trends in technology and
  holistic development.
</p>

<p>
  Beginning with an orphanage (BalBhavan) and a trade school in carpentry, the
  Agnel Ashram (set up in 1957 at Bandra, Mumbai) has today grown into a
  full-fledged Technical Complex. The Institute today proudly bears the
  founder's name as a fitting tribute to his impassioned faith in
  highly-qualified and fully-trained engineers and technicians in the service of
  the nation.
</p>

