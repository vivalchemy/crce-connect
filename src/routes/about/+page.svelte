<p>
  The Institute started with only one programme in Production Engineering in
  1984 with an intake capacity of 60 students. In 1987, the programme in
  Electronics Engineering was started with an intake capacity of 60 students,
  followed by Computer Engineering in 1991 with an intake capacity of 60
  students. In 2001, the programme in Information Technology was started with an
  intake capacity of 30 students, which was later increased to 60 in 2010. The
  College started offering Master's Degree in Electronics Engineering and
  Mechanical Engineering in the year 2005, each having an intake capacity of 18.
  Doctoral programs in Electronics Engineering and Mechanical Engineering
  commenced in 2014 and 2015 respectively. From the year 2019, a new
  Undergraduate degree programme in Mechanical Engineering has been started with
  an intake capacity of 60. Also, the BE undergraduate programme in Electronics
  Engineering has been changed to Electronics and Computer Science. The
  Undergraduate programme in Information Technology has been merged with
  Computer Engineering. From the year 2020, college is offering a new
  undergraduate programme in the emerging area of 'AI & Data Science'. Also, a
  Doctoral program in Computer Engineering started in the year 2023.
</p>

<p>
  The Institute has been consistently ranked highly in major surveys like TOI,
  INDIA TODAY, Career 360, The Week, and Education World to name a few. The
  College is ranked by NIRF in 2017 and 2020. Based on the Outcome-Based
  Education (OBE) parameters and criteria, three programmes of the institute
  have been accredited by the National Board of Accreditation (NBA-India) since
  2017-2018. The current accreditation is valid until June 2023. The college is
  also NAAC accredited with grade 'A'.
</p>

